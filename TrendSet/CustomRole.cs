        public override string[] GetRolesForUser(string username)
        {
            TrendSetContext context = new TrendSetContext();
            var result = (from roles in context.Roles
                          join role in context.RoleLoginMappings on roles.RoleId equals role.RoleId
                          join user in context.UserDetails on role.UserId equals user.UserId
                          where user.UserName == username
                          select roles.RoleName).ToArray();
            return result;
        }

